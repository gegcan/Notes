import UIKit

extension UIFont {
    
    static func avenirNext() -> UIFont? {
        return UIFont.init(name: "AvenirNext-Regular", size: 16)
    }
    
    static func avenirNextBold() -> UIFont? {
        return UIFont.init(name: "AvenirNext-Bold", size: 18)
    }
}
